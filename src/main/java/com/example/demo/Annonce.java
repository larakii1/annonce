package com.example.demo;

public class Annonce {
	int numeroAnnonce;
	String annonceTitle; 
	String categorie ;
	
	public int getNumeroAnnonce() {
		return numeroAnnonce;
	}
	public void setNumeroAnnonce(int numeroAnnonce) {
		this.numeroAnnonce = numeroAnnonce;
	}
	public String getAnnonceTitle() {
		return annonceTitle;
	}
	public void setAnnonceTitle(String annonceTitle) {
		this.annonceTitle = annonceTitle;
	}
	public String getCategorie() {
		return categorie;
	}
	public void setCategorie(String categorie) {
		this.categorie = categorie;
	}
	
}
